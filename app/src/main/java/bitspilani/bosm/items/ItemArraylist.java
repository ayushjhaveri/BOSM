package bitspilani.bosm.items;

public class ItemArraylist {
    int amount;
    String college;
    boolean power_bid;
    String user_id;

    public int getAmount() {
        return amount;
    }

    public String getCollege() {
        return college;
    }

    public boolean isPower_bid() {
        return power_bid;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public void setCollege(String college) {
        this.college = college;
    }

    public void setPower_bid(boolean power_bid) {
        this.power_bid = power_bid;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }
}
